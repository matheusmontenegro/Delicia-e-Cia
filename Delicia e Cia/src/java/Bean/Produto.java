
package Bean;

public class Produto {
    private int idProduto;
    private String tipo;
    private String nomeProduto;
    private int quantidade;
    private String descricao;
    private String precoUnidade;
    private String precoCento;
    private String foto;

    /**
     * @return the tipo
     */
    public String getTipo() {
        return tipo;
    }

    /**
     * @param tipo the tipo to set
     */
    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    /**
     * @return the nomeProduto
     */
    public String getNomeProduto() {
        return nomeProduto;
    }

    /**
     * @param nomeProduto the nomeProduto to set
     */
    public void setNomeProduto(String nomeProduto) {
        this.nomeProduto = nomeProduto;
    }

    /**
     * @return the quantidade
     */
    public int getQuantidade() {
        return quantidade;
    }

    /**
     * @param quantidade the quantidade to set
     */
    public void setQuantidade(int quantidade) {
        this.quantidade = quantidade;
    }

    /**
     * @return the descricao
     */
    public String getDescricao() {
        return descricao;
    }

    /**
     * @param descricao the descricao to set
     */
    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }

    /**
     * @return the precoUnidade
     */
    public String getPrecoUnidade() {
        return precoUnidade;
    }

    /**
     * @param precoUnidade the precoUnidade to set
     */
    public void setPrecoUnidade(String precoUnidade) {
        this.precoUnidade = precoUnidade;
    }

    /**
     * @return the precoCento
     */
    public String getPrecoCento() {
        return precoCento;
    }

    /**
     * @param precoCento the precoCento to set
     */
    public void setPrecoCento(String precoCento) {
        this.precoCento = precoCento;
    }

    /**
     * @return the foto
     */
    public String getFoto() {
        return foto;
    }

    /**
     * @param foto the foto to set
     */
    public void setFoto(String foto) {
        this.foto = foto;
    }

    /**
     * @return the idProduto
     */
    public int getIdProduto() {
        return idProduto;
    }

    /**
     * @param idProduto the idProduto to set
     */
    public void setIdProduto(int idProduto) {
        this.idProduto = idProduto;
    }
    
}
